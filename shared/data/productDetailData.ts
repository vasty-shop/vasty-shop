// Helper function to get product by ID
export const getProductDetailById = (id: string): ProductDetail | undefined => {
  // Handle both numeric IDs (1, 2, 3) and full IDs (product-detail-1)
  // Convert numeric ID to product-detail-X format
  let searchId = id;
  if (/^\d+$/.test(id)) {
    searchId = `product-detail-${id}`;
  }

  return productDetailData.find((product) => product.id === searchId);
};

// Helper function to get related products
export const getRelatedProducts = (productId: string): ProductDetail[] => {
  const product = getProductDetailById(productId);
  if (!product) return [];

  return productDetailData.filter((p) =>
    product.relatedProductIds.includes(p.id)
  );
};

// Helper function to get reviews by rating
export const getReviewsByRating = (
  productId: string,
  rating: number
): ProductReview[] => {
  const product = getProductDetailById(productId);
  if (!product) return [];

  return product.reviews.filter((review) => review.rating === rating);
};

// Helper function to calculate average rating
export const calculateAverageRating = (reviews: ProductReview[]): number => {
  if (reviews.length === 0) return 0;
  const sum = reviews.reduce((acc, review) => acc + review.rating, 0);
  return Math.round((sum / reviews.length) * 10) / 10;
};

// Helper function to get rating distribution
export const getRatingDistribution = (
  reviews: ProductReview[]
): Record<number, number> => {
  const distribution: Record<number, number> = { 5: 0, 4: 0, 3: 0, 2: 0, 1: 0 };

  reviews.forEach((review) => {
    distribution[review.rating]++;
  });

  return distribution;
};
